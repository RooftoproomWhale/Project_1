CREATE TABLE ADDR
(
	CODE number NOT NULL,
	SIDO nvarchar2(20) NOT NULL,
	SI_GOON_GO nvarchar2(20),
	DONG nvarchar2(20),
	DONG_SUB nvarchar2(20),
	LAT number NOT NULL,
	LNG number NOT NULL,
	PRIMARY KEY (CODE)
);


CREATE TABLE AUTH_SECURITY
(
	MEM_EMAIL varchar2(20) NOT NULL,
	ROLE varchar2(10) NOT NULL,
	ENABLE number DEFAULT 0 NOT NULL
);


CREATE TABLE CHATQNA
(
	QNA_NO number NOT NULL,
	QUESTION nvarchar2(100) NOT NULL,
	ANSWER nvarchar2(200) NOT NULL,
	PRIMARY KEY (QNA_NO)
);


CREATE TABLE CHAT_HISTORY
