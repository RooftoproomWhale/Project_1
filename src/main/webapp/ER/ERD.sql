);


CREATE TABLE MEDICINE
(
	MEDI_TYPE nvarchar2(20) NOT NULL,
	MEDI_CODE number NOT NULL,
	MEDI_NAME nvarchar2(30) NOT NULL,
	MEDI_LOOKS nvarchar2(50) NOT NULL,
	MEDI_MATERIAL nvarchar2(100) NOT NULL,
	MEDI_EFFICIENT nvarchar2(100) NOT NULL,
	VALID_TERM nvarchar2(50) NOT NULL,
	SIDEEFFECT nvarchar2(100),
	PRIMARY KEY (MEDI_CODE)
);


CREATE TABLE MEMBER
(
	MEM_EMAIL varchar2(20) NOT NULL,
	MEM_PWD varchar2(20) NOT NULL,
	MEM_NAME nvarchar2(10) NOT NULL,
